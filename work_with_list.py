# python's for loop
magicians = ['alice', 'john', 'michael', 'suzy']
for magician in magicians:
        print(magician.capitalize() + " has performed some tricks.")
        print("I am looking forward to your next trick, " + magician.title() + ".\n")

print("Thank you, everyone for coming. That was a great magic show.")

